package com.pengpeng.stargame.exception;

public class ConstructException extends GameException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3057108928699203343L;

	public ConstructException() {
	}

	public ConstructException(String message, Throwable cause) {
		super(message, cause);
	}

	public ConstructException(String message) {
		super(message);
	}

	public ConstructException(Throwable cause) {
		super(cause);
	}

}
