@Entity
@Table(name="gm_player_donation_action")
public class PlayerDonationActionModel extends Model{

    @Column
    private int gameCoin;
    @Column
    private int goldCoin;

    public PlayerDonationActionModel() {
    }

    public PlayerDonationActionModel(String uid, String pid, Date date) {
        super(uid, pid, date);
    }

    public int getGameCoin() {
        return gameCoin;
    }

    public void setGameCoin(int gameCoin) {
        this.gameCoin = gameCoin;
    }

    public int getGoldCoin() {
        return goldCoin;
    }

    public void setGoldCoin(int goldCoin) {
        this.goldCoin = goldCoin;
    }
}
