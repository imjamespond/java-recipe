@Entity
@Table(name="gm_player_lottery_action")
public class PlayerLotteryActionModel extends Model {
    @Column
    private String itemId;
    @Column
    private int num;
    @Column
    private int gameCoin;

    public PlayerLotteryActionModel() {
    }

    public PlayerLotteryActionModel(String uid, String pid, Date date) {
        super(uid, pid, date);
    }

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getGameCoin() {
        return gameCoin;
    }

    public void setGameCoin(int gameCoin) {
        this.gameCoin = gameCoin;
    }
}
